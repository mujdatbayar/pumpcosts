    Double[] calculateSubmersibleInvestmentCost(){
        int years = study.getYearCount();
        int typeCount = study.getSubmersibleTypeCount();

        BaseCosts2 bCost = study.getSubmersibleBaseCosts();
        Vector<Vector<Double>> costs = bCost.getPumpInvestmentCosts();
        Vector<Vector<Double>> screenCost = bCost.getScreenCosts();
        Vector<Vector<Double>> drainageCost = bCost.getDrainageChannelsCosts();
        Vector<Vector<Double>> agitator = bCost.getAgitator();
        Vector<Vector<Double>> pumpHouse = bCost.getPumpHouse();
        Vector<Vector<Integer>> pumpAmounts = study.getSubmersiblePumpAmounts();
        Double[] column = new Double[years];
        int[] prevs = new int[typeCount];
        for(int k = 0; k < years; k++) {
            if(pumpAmounts == null || pumpAmounts.size() <= k) {
                    column[k] = 0.0;
                    continue;
                }
            column[k] = 0.0;
            Vector<Integer> row = pumpAmounts.get(k);
            Vector<Double> values = null;
            Vector<Double> values2 = null;
            Vector<Double> values3 = null;
            Vector<Double> values4 = null;
            Vector<Double> values5 = null;
                    
            if(costs != null && costs.size() > k ){
                values = costs.get(k);
            }
            if(screenCost != null && screenCost.size() > k ){
                values2 = screenCost.get(k);
            }
            if(drainageCost != null && drainageCost.size() > k ){
                values3 = drainageCost.get(k);
            }
            if(agitator != null && agitator.size() > k ){
                values4 = agitator.get(k);
            }
            if(pumpHouse != null && pumpHouse.size() > k ){
                values5 = pumpHouse.get(k);
            }

            for(int t=0; t< typeCount; t++){
                int cnt = row.get(t) - prevs[t];
                prevs[t] = row.get(t);
                if(values != null && values.size() > t) {
                    column[k] = column[k] + cnt * values.get(t);
                }
                if(values2 != null && values2.size() > t) {
                    column[k] = column[k] + values2.get(t);
                }
                if(values3 != null && values3.size() > t) {
                    column[k] = column[k] + values3.get(t);
                }
                if(values4 != null && values4.size() > t) {
                    column[k] = column[k] + values4.get(t);
                }
                if(values5 != null && values5.size() > t) {
                    column[k] = column[k] + values5.get(t);
                }

            }
            
        }
        return column;
    }
      
